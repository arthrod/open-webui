@router.get("/config")
async def get_task_config(request: Request, user=Depends(get_verified_user)):
    """
    Retrieves the current task configuration from the application's state.
    
    This asynchronous function extracts various task-related configuration parameters from the FastAPI application's state,
    including model specifications and generation templates for titles, image prompts, autocompletion, tags, and query generation.
    It requires a verified user, enforced via dependency injection of the `get_verified_user` dependency.
    
    Parameters:
        request (Request): The incoming FastAPI request, which provides access to the application's configuration state.
        user: The verified user object provided by the dependency injection system.
    
    Returns:
        dict: A dictionary containing the following configuration keys:
            - TASK_MODEL: The primary task model identifier.
            - TASK_MODEL_EXTERNAL: Indicator for an external task model.
            - TITLE_GENERATION_PROMPT_TEMPLATE: Template used for generating titles.
            - IMAGE_PROMPT_GENERATION_PROMPT_TEMPLATE: Template used for generating image prompts.
            - ENABLE_AUTOCOMPLETE_GENERATION: Boolean flag indicating if autocomplete generation is enabled.
            - AUTOCOMPLETE_GENERATION_INPUT_MAX_LENGTH: Maximum allowed input length for autocomplete generation.
            - TAGS_GENERATION_PROMPT_TEMPLATE: Template used for generating tags.
            - ENABLE_TAGS_GENERATION: Boolean flag indicating if tags generation is enabled.
            - ENABLE_SEARCH_QUERY_GENERATION: Boolean flag indicating if search query generation is enabled.
            - ENABLE_RETRIEVAL_QUERY_GENERATION: Boolean flag indicating if retrieval query generation is enabled.
            - QUERY_GENERATION_PROMPT_TEMPLATE: Template used for generating queries.
            - TOOLS_FUNCTION_CALLING_PROMPT_TEMPLATE: Template used for tools function calling.
    
    Example:
        >>> from fastapi import Request
        >>> request = Request(scope={"type": "http", "app": your_app})
        >>> config = await get_task_config(request)
        >>> print(config)
    """
    return {
        "TASK_MODEL": request.app.state.config.TASK_MODEL,
        "TASK_MODEL_EXTERNAL": request.app.state.config.TASK_MODEL_EXTERNAL,
        "TITLE_GENERATION_PROMPT_TEMPLATE": request.app.state.config.TITLE_GENERATION_PROMPT_TEMPLATE,
        "IMAGE_PROMPT_GENERATION_PROMPT_TEMPLATE": request.app.state.config.IMAGE_PROMPT_GENERATION_PROMPT_TEMPLATE,
        "ENABLE_AUTOCOMPLETE_GENERATION": request.app.state.config.ENABLE_AUTOCOMPLETE_GENERATION,
        "AUTOCOMPLETE_GENERATION_INPUT_MAX_LENGTH": request.app.state.config.AUTOCOMPLETE_GENERATION_INPUT_MAX_LENGTH,
        "TAGS_GENERATION_PROMPT_TEMPLATE": request.app.state.config.TAGS_GENERATION_PROMPT_TEMPLATE,
        "ENABLE_TAGS_GENERATION": request.app.state.config.ENABLE_TAGS_GENERATION,
        "ENABLE_SEARCH_QUERY_GENERATION": request.app.state.config.ENABLE_SEARCH_QUERY_GENERATION,
        "ENABLE_RETRIEVAL_QUERY_GENERATION": request.app.state.config.ENABLE_RETRIEVAL_QUERY_GENERATION,
        "QUERY_GENERATION_PROMPT_TEMPLATE": request.app.state.config.QUERY_GENERATION_PROMPT_TEMPLATE,
        "TOOLS_FUNCTION_CALLING_PROMPT_TEMPLATE": request.app.state.config.TOOLS_FUNCTION_CALLING_PROMPT_TEMPLATE,
    }


