@router.post("/queries/completions")
async def generate_queries(
    request: Request, form_data: dict, user=Depends(get_verified_user)
):

    """
    Generate queries based on the provided form data.
    
    This asynchronous function generates queries for either web search or retrieval based on the 'type' specified in the form_data. It first verifies whether query generation is enabled for the specified type in the application configuration. It then validates the provided model identifier against the available models and retrieves the appropriate task model if a custom model is configured for the user. A query generation prompt template is selected from the configuration or a default is used, and the template is populated with the user messages and details. Finally, the function constructs a payload and calls generate_chat_completion to generate the queries. In case of errors during processing, it returns a JSONResponse with the error details.
    
    Parameters:
        request (Request): The FastAPI request object containing state, configuration, and models.
        form_data (dict): A dictionary with keys including:
            - "type" (str): The type of query generation request ("web_search" or "retrieval").
            - "model" (str): The identifier of the model to be used.
            - "messages" (list): The message history to generate the query.
            - "chat_id" (Optional[str]): An optional chat identifier.
        user: The verified user making the request (dependency injected via get_verified_user); expected to have at least 'email' and 'name' attributes.
    
    Returns:
        The chat completion response returned by generate_chat_completion if successful, or a JSONResponse with an error message and a 400 status code if an exception occurs.
    
    Raises:
        HTTPException: If the requested query generation (web_search or retrieval) is disabled in the application configuration.
        HTTPException: If the provided model identifier is not found among the available models.
    """
    type = form_data.get("type")
    if type == "web_search":
        if not request.app.state.config.ENABLE_SEARCH_QUERY_GENERATION:
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail=f"Search query generation is disabled",
            )
    elif type == "retrieval":
        if not request.app.state.config.ENABLE_RETRIEVAL_QUERY_GENERATION:
            raise HTTPException(
                status_code=status.HTTP_400_BAD_REQUEST,
                detail=f"Query generation is disabled",
            )

    models = request.app.state.MODELS

    model_id = form_data["model"]
    if model_id not in models:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Model not found",
        )

    # Check if the user has a custom task model
    # If the user has a custom task model, use that model
    task_model_id = get_task_model_id(
        model_id,
        request.app.state.config.TASK_MODEL,
        request.app.state.config.TASK_MODEL_EXTERNAL,
        models,
    )

    log.debug(
        f"generating {type} queries using model {task_model_id} for user {user.email}"
    )

    if (request.app.state.config.QUERY_GENERATION_PROMPT_TEMPLATE).strip() != "":
        template = request.app.state.config.QUERY_GENERATION_PROMPT_TEMPLATE
    else:
        template = DEFAULT_QUERY_GENERATION_PROMPT_TEMPLATE

    content = query_generation_template(
        template, form_data["messages"], {"name": user.name}
    )

    payload = {
        "model": task_model_id,
        "messages": [{"role": "user", "content": content}],
        "stream": False,
        "metadata": {
            "task": str(TASKS.QUERY_GENERATION),
            "task_body": form_data,
            "chat_id": form_data.get("chat_id", None),
        },
    }

    try:
        return await generate_chat_completion(request, form_data=payload, user=user)
    except Exception as e:
        return JSONResponse(
            status_code=status.HTTP_400_BAD_REQUEST,
            content={"detail": str(e)},
        )


